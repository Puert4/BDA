    public boolean LogIn() {
        String correo = txt_email.getText();
        String passcode = new String(txt_passcode.getPassword()); // Get password as string

        int userId = verificadores.verificarCredenciales(correo, passcode);

        if (userId != -1) { // Check if user ID is valid
            // If valid, store the user ID in usuarioNuevo object
//            usuarioNuevo.setIdUsuario(userId);
            usuarioNuevo.setId_usuario(userId);
            // Handle successful login
            return true;
        } else {
            // Handle unsuccessful login
            JOptionPane.showMessageDialog(null, "Invalid email or passcode!");
            return false;
        }
    }

